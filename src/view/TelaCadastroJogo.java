    private void jBFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBFinalizarActionPerformed
        // TODO add your handling code here:
        Tipo tipo = new Tipo();
        Tipo tipoInser = new Tipo();
        TipoDAO tipdao = new TipoDAO();
        
        if(jTJogo.getSelectedRow()!=-1){   
             Jogo jogo = new Jogo();
             JogoDAO dao = new JogoDAO();
             
             
             if(txtStatus.getSelectedItem().equals("V")){
                    jogo.setStatus("VITORIA");             
             }else{
                 jogo.setStatus("DERROTA");
             }
             
           
             jogo.setCodjog(Integer.parseInt(jTJogo.getValueAt(jTJogo.getSelectedRow(), 0).toString()));
             dao.updateStatus(jogo);
             
             
             tipo.setCodtip(dao.selecionarCodTip(jogo));
             tipoInser=tipdao.readRetorno(tipo);
             tipdao.updateTipo(tipoInser);
             readJTable();
        }
    }//GEN-LAST:event_jBFinalizarActionPerformed
    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTJogo.getModel();
        modelo.setNumRows(0);
        JogoDAO dao = new JogoDAO();
        
        for(Jogo j : dao.read(this.getCodTip())){
            modelo.addRow(new Object[]{
                j.getCodjog(),
                j.getTimeA(),
                j.getTimeB(),
                j.getAposta(),
                j.getAfavor(),
                j.getData(),
                j.getOdds(),
                j.getStatus(),
                j.getRetorno()
            });
        }    
    }

