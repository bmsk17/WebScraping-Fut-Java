    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        // TODO add your handling code here:
        Jogo jogo = new Jogo();
        JogoDAO dao = new JogoDAO();
        Tipo tipo = new Tipo();
        Tipo tipoInser = new Tipo();
        TipoDAO tipdao = new TipoDAO();
        
        jogo.setCodjog(Integer.parseInt(jTJogo.getValueAt(jTJogo.getSelectedRow(), 0).toString()));
        dao.excluir(jogo);
        
        DefaultTableModel dtmJogo = (DefaultTableModel) jTJogo.getModel();
        dtmJogo.removeRow(jTJogo.getSelectedRow()); 
        
        tipo.setCodtip(dao.selecionarCodTip(jogo));
        tipoInser=tipdao.readRetorno(tipo);
        tipdao.updateTipo(tipoInser);
        
        
        
        
    }//GEN-LAST:event_jBExcluirActionPerformed

    private void jBAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAtualizarActionPerformed
        // TODO add your handling code here:
        if(jTJogo.getSelectedRow()!=-1){   
             Jogo jogo = new Jogo();
             JogoDAO dao = new JogoDAO();
             Tipo tipo = new Tipo();
             Tipo tipoInser = new Tipo();
             TipoDAO tipdao = new TipoDAO();
             
             jogo.setTimeA(txtCasa.getText());
             jogo.setTimeB(txtVisitante.getText());
             jogo.setAposta(Double.parseDouble(txtValor.getText()));
             jogo.setData(txtData.getText());
             jogo.setOdds(Double.parseDouble(txtOdds.getText()));
             jogo.setAfavor(txtafavor.getSelectedItem().toString());
             jogo.setRetorno((jogo.getAposta()*jogo.getOdds())-jogo.getAposta());

             
             jogo.setCodjog(Integer.parseInt(jTJogo.getValueAt(jTJogo.getSelectedRow(), 0).toString()));
             dao.update(jogo);
             
             tipo.setCodtip(dao.selecionarCodTip(jogo));
             tipoInser=tipdao.readRetorno(tipo);
             tipdao.updateTipo(tipoInser);
             
             jTJogo.setValueAt(txtCasa.getText(), jTJogo.getSelectedRow(), 1);
             jTJogo.setValueAt(txtVisitante.getText(), jTJogo.getSelectedRow(), 2);
             jTJogo.setValueAt(txtValor.getText(), jTJogo.getSelectedRow(), 3);
             jTJogo.setValueAt(txtafavor.getSelectedItem().toString(), jTJogo.getSelectedRow(), 4);
             jTJogo.setValueAt(txtData.getText(), jTJogo.getSelectedRow(), 5);
             jTJogo.setValueAt(txtOdds.getText(), jTJogo.getSelectedRow(), 6);
             
             txtCasa.setText("");
             txtVisitante.setText("");
             txtValor.setText("");
             txtafavor.setSelectedIndex(0);
             txtData.setText("");
             txtOdds.setText("");
             
            
             readJTable();
        }
    }//GEN-LAST:event_jBAtualizarActionPerformed

